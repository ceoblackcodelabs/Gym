// ── Toast ───────────────────────────────────────
LOOPSTER.toast = (msg, type = 'default', duration = 3000) => {
  let container = document.querySelector('.toast-container');
  if (!container) {
    container = document.createElement('div');
    container.className = 'toast-container';
    document.body.appendChild(container);
  }
  const t = document.createElement('div');
  t.className = `toast ${type}`;
  t.textContent = msg;
  container.appendChild(t);
  requestAnimationFrame(() => { requestAnimationFrame(() => t.classList.add('show')); });
  setTimeout(() => {
    t.classList.remove('show');
    setTimeout(() => t.remove(), 400);
  }, duration);
};

// ── Mobile Nav ──────────────────────────────────
LOOPSTER.initNav = () => {
  const btn = document.getElementById('menuBtn');
  const drawer = document.getElementById('mobileNav');
  if (!btn || !drawer) return;
  btn.addEventListener('click', () => {
    btn.classList.toggle('open');
    drawer.classList.toggle('open');
  });
  // Close on outside click
  document.addEventListener('click', (e) => {
    if (!btn.contains(e.target) && !drawer.contains(e.target)) {
      btn.classList.remove('open');
      drawer.classList.remove('open');
    }
  });
  // Active link
  const current = location.pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('.main-nav a, .mobile-nav a').forEach(a => {
    if (a.getAttribute('href') === current) a.classList.add('active');
  });
};

// ── Search overlay ───────────────────────────────
LOOPSTER.initSearch = () => {
  const btn = document.getElementById('searchBtn');
  const overlay = document.getElementById('searchOverlay');
  const input = document.getElementById('searchInput');
  if (!btn || !overlay) return;
  const open = () => { overlay.classList.add('open'); input && input.focus(); };
  const close = () => overlay.classList.remove('open');
  btn.addEventListener('click', open);
  overlay.addEventListener('click', (e) => { if (e.target === overlay) close(); });
  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') close();
    if ((e.ctrlKey || e.metaKey) && e.key === 'k') { e.preventDefault(); open(); }
  });
  document.getElementById('searchClose')?.addEventListener('click', close);
  input?.addEventListener('keydown', (e) => {
    if (e.key === 'Enter') {
      const q = input.value.trim();
      if (q) { window.location.href = `blog-list.html?q=${encodeURIComponent(q)}`; }
    }
  });
};

// ── Back to top ──────────────────────────────────
LOOPSTER.initBackToTop = () => {
  const btn = document.getElementById('backToTop');
  if (!btn) return;
  window.addEventListener('scroll', () => {
    btn.classList.toggle('visible', window.scrollY > 400);
  }, { passive: true });
  btn.addEventListener('click', () => window.scrollTo({ top: 0, behavior: 'smooth' }));
};

// ── Ticker duplication ───────────────────────────
LOOPSTER.initTicker = () => {
  const track = document.querySelector('.ticker-track');
  if (!track) return;
  track.innerHTML += track.innerHTML; // duplicate for seamless loop
};

// ── Category strip active ────────────────────────
LOOPSTER.initCategoryStrip = () => {
  const params = new URLSearchParams(location.search);
  const cat = params.get('cat') || '';
  document.querySelectorAll('.category-strip a').forEach(a => {
    const href = a.getAttribute('href');
    if (href && href.includes(cat) && cat) a.classList.add('active');
    if (!cat && href && href.endsWith('blog-list.html')) a.classList.add('active');
  });
};

// ── Format number ────────────────────────────────
LOOPSTER.formatNum = (n) => n >= 1000 ? (n/1000).toFixed(1) + 'K' : n.toString();

// ── Share ────────────────────────────────────────
LOOPSTER.share = async (title, url) => {
  if (navigator.share) {
    try { await navigator.share({ title, url }); return; } catch(e) {}
  }
  try {
    await navigator.clipboard.writeText(url);
    LOOPSTER.toast('Link copied to clipboard!', 'success');
  } catch {
    LOOPSTER.toast('Could not copy link', 'error');
  }
};

// ── Init on DOM ready ────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  LOOPSTER.initNav();
  LOOPSTER.initSearch();
  LOOPSTER.initBackToTop();
  LOOPSTER.initTicker();
  LOOPSTER.initCategoryStrip();
});
