// ── LocalStorage likes ──────────────────────────
LOOPSTER.getLikes = () => JSON.parse(localStorage.getItem('loopster_likes') || '{}');
LOOPSTER.saveLikes = (likes) => localStorage.setItem('loopster_likes', JSON.stringify(likes));
LOOPSTER.isLiked = (id) => !!LOOPSTER.getLikes()[id];
LOOPSTER.toggleLike = (id) => {
  const likes = LOOPSTER.getLikes();
  if (likes[id]) { delete likes[id]; } else { likes[id] = true; }
  LOOPSTER.saveLikes(likes);
  return !!likes[id];
};

